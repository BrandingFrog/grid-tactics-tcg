"""
CLI entry point for syncing card pages and art to the Grid Tactics Wiki.

Orchestrates card page upsert, art upload, and verification against the
live Railway wiki.  All card data comes from ``data/cards/*.json``; wikitext
is generated by :mod:`sync.sync_cards`.

Usage::

    cd wiki
    python -m sync.sync_wiki --all-cards         # Upsert all cards + upload art
    python -m sync.sync_wiki --card ratchanter    # Upsert single card by card_id
    python -m sync.sync_wiki --all-cards --dry-run  # Show what would change
    python -m sync.sync_wiki --verify             # Check Category:Card count
    python -m sync.sync_wiki --upload-art         # Upload art PNGs only
    python -m sync.sync_wiki --verify-deep        # Full verification suite
"""

