"""
CLI entry point for syncing card pages and art to the Grid Tactics Wiki.

Orchestrates card page upsert, art upload, and verification against the
live Railway wiki.  All card data comes from ``data/cards/*.json``; wikitext
is generated by :mod:`sync.sync_cards`.

Usage::

    cd wiki
    python -m sync.sync_wiki --all-cards         # Upsert all cards + upload art
    python -m sync.sync_wiki --card ratchanter    # Upsert single card by card_id
    python -m sync.sync_wiki --all-cards --dry-run  # Show what would change
    python -m sync.sync_wiki --verify             # Check Category:Card count
    python -m sync.sync_wiki --upload-art         # Upload art PNGs only
    python -m sync.sync_wiki --verify-deep        # Full verification suite
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

from sync.client import MissingCredentialsError, get_site
from sync.sync_cards import build_card_name_map, card_to_wikitext

# ---------------------------------------------------------------------------
# Repo layout
# ---------------------------------------------------------------------------

_REPO_ROOT = Path(__file__).resolve().parent.parent.parent
_CARDS_DIR = _REPO_ROOT / "data" / "cards"
_ART_DIR = _REPO_ROOT / "src" / "grid_tactics" / "server" / "static" / "art"

# ---------------------------------------------------------------------------
# Core functions
# ---------------------------------------------------------------------------


def load_all_cards(cards_dir: Path) -> list[dict]:
    """Load all card JSONs from *cards_dir*, sorted by card_id."""
    cards: list[dict] = []
    for path in sorted(cards_dir.glob("*.json")):
        try:
            card = json.loads(path.read_text(encoding="utf-8"))
            cards.append(card)
        except (json.JSONDecodeError, KeyError) as exc:
            print(f"  warning: skipping {path.name}: {exc}")
    cards.sort(key=lambda c: c.get("card_id", ""))
    return cards


def upsert_card_page(
    site,
    card: dict,
    name_map: dict[str, str],
    art_dir: Path,
    dry_run: bool = False,
) -> dict:
    """Upsert a single card page on the wiki.

    Returns dict with card_id, page title, and status.
    """
    card_id = card.get("card_id", "")
    card_name = card.get("name", "")
    art_exists = (art_dir / f"{card_id}.png").exists()
    wikitext = card_to_wikitext(card, name_map, art_exists=art_exists)
    page_title = f"Card:{card_name}"

    if dry_run:
        page = site.pages[page_title]
        if not page.exists:
            return {"card_id": card_id, "page": page_title, "status": "would-create"}
        current = page.text()
        if current.rstrip() == wikitext.rstrip():
            return {"card_id": card_id, "page": page_title, "status": "unchanged"}
        return {"card_id": card_id, "page": page_title, "status": "would-update"}

    page = site.pages[page_title]
    summary = f"sync {card_name} from data/cards/{card_id}.json"

    if not page.exists:
        page.edit(wikitext, summary=summary)
        return {"card_id": card_id, "page": page_title, "status": "created"}

    current = page.text()
    if current.rstrip() == wikitext.rstrip():
        return {"card_id": card_id, "page": page_title, "status": "unchanged"}

    page.edit(wikitext, summary=summary)
    return {"card_id": card_id, "page": page_title, "status": "updated"}


def upload_card_art(
    site,
    card_id: str,
    card_name: str,
    art_dir: Path,
    dry_run: bool = False,
) -> str:
    """Upload card art PNG to the wiki.  Returns status string."""
    art_path = art_dir / f"{card_id}.png"
    if not art_path.exists():
        return "no-art"
    if dry_run:
        return "dry-run"
    try:
        with open(art_path, "rb") as f:
            site.upload(
                file=f,
                filename=f"{card_id}.png",
                description=f"Card art for [[Card:{card_name}]].",
                ignore=True,
                comment="sync card art",
            )
        return "uploaded"
    except Exception as exc:
        # MediaWiki returns fileexists-no-change for exact duplicate uploads.
        # Treat as "unchanged" for idempotency.
        exc_str = str(exc)
        if "fileexists-no-change" in exc_str or "duplicate" in exc_str.lower():
            return "unchanged"
        print(f"  art upload error ({card_id}): {exc}")
        return "error"


def verify_card_count(site, expected: int) -> bool:
    """Check that Category:Card has the expected number of members."""
    try:
        cat = site.categories["Card"]
        actual = sum(1 for _ in cat)
    except Exception as exc:
        print(f"  error reading Category:Card: {exc}")
        return False
    print(f"  Category:Card: {actual} pages (expected {expected})")
    return actual == expected


