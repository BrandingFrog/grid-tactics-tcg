// React-prompt mode: 'auto' | 'off'. Persisted to localStorage so
// the choice survives reloads. The auto-skip in renderActionBar reads
// _reactPromptMode() to decide whether to fire PASS automatically.
// 'on' (never auto-skip) REMOVED per user 2026-07-10 — the fake
// response-wait hourglass made it redundant; legacy stored 'on'
// values fall back to 'auto'.
var REACT_MODE_KEY = 'gridtactics.reactMode';
var REACT_MODE_CYCLE = { 'auto': 'off', 'off': 'auto' };
function _reactPromptMode() {
    try {
        var v = localStorage.getItem(REACT_MODE_KEY);
        if (v === 'auto' || v === 'off') return v;
    } catch (e) { /* localStorage unavailable */ }
    return 'auto';
}
function _setReactPromptMode(mode) {
    try { localStorage.setItem(REACT_MODE_KEY, mode); } catch (e) { /* defensive */ }
    _renderReactModeButtons();
    // Re-render the action bar so the auto-skip reconsiders under the new mode.
    if (typeof renderActionBar === 'function') {
        try { renderActionBar(); } catch (e) { /* defensive */ }
    }
}
function _renderReactModeButtons() {
    var mode = _reactPromptMode();
    // Animation speed toggle (MCQ 2026-07-11): 1x -> 2x -> 4x -> 1x,
    // persisted per browser.
    try {
        var savedSpeed = parseInt(localStorage.getItem('gt-anim-speed') || '1', 10);
        window.__animSpeed = (savedSpeed === 2 || savedSpeed === 4) ? savedSpeed : 1;
    } catch (e) { window.__animSpeed = 1; }
    var speedBtn = document.getElementById('anim-speed-btn');
    var speedVal = document.getElementById('anim-speed-value');
    function _paintSpeed() {
        if (speedVal) speedVal.innerHTML = (window.__animSpeed || 1) + '&times;';
    }
    _paintSpeed();
    // _renderReactModeButtons re-runs on every mode change — bind once.
    if (speedBtn && !speedBtn.dataset.bound) {
        speedBtn.dataset.bound = '1';
        speedBtn.addEventListener('click', function() {
            var cur = window.__animSpeed || 1;
            window.__animSpeed = cur === 1 ? 2 : (cur === 2 ? 4 : 1);
            try { localStorage.setItem('gt-anim-speed', String(window.__animSpeed)); } catch (e) {}
            _paintSpeed();
            // AI-watch pacing: tell the server so the exhibition stepper
            // matches (no-op for normal games/spectates).
            try {
                if (typeof socket !== 'undefined' && socket) {
                    socket.emit('set_watch_speed', { speed: window.__animSpeed });
                }
            } catch (e) { /* defensive */ }
        });
    }

    ['react-mode-btn', 'sandbox-react-mode-btn'].forEach(function(id) {
        var btn = document.getElementById(id);
        if (!btn) return;
        btn.dataset.mode = mode;
        var val = btn.querySelector('.react-mode-value');
        if (val) val.textContent = mode.toUpperCase();
    });
}
function _wireReactModeButtonsOnce() {
    if (window.__reactModeWired) return;
    window.__reactModeWired = true;
    ['react-mode-btn', 'sandbox-react-mode-btn'].forEach(function(id) {
        var btn = document.getElementById(id);
        if (!btn) return;
        btn.addEventListener('click', function() {
            var current = _reactPromptMode();
            _setReactPromptMode(REACT_MODE_CYCLE[current] || 'auto');
        });
    });
    _renderReactModeButtons();
}

