// Card art originals are 1024x1024 PNGs (1.5–3 MB each); the in-game UI
// renders them at ~280x420 max. Loading and decoding the originals for
// every hand card / board minion / spell stage slam thrashes the GPU
// texture cache and causes visible lag on slam-in animations. We ship
// 512px WebP thumbnails (~50 KB each, generated by
// scripts/generate_card_thumbs.py) and use them by default. Tooltip
// previews lazy-upgrade to the full PNG once visible (see
// _lazyUpgradeArt).
function _cardArtUrl(card_id, full) {
    if (!card_id) return '';
    return full
        ? '/static/art/' + card_id + '.png'
        : '/static/art/' + card_id + '.thumb.webp';
}
// Swap the bg-image of a .card-art element from the thumb to the full
// PNG once the full PNG is decoded. Safe to call on already-upgraded
// elements (no-op). Used by populateTooltip so the hover preview shows
// crisp art once it's actually being looked at.
function _lazyUpgradeArt(rootEl, cardId) {
    if (!rootEl || !cardId) return;
    // Full-art frame uses .cf2-artbg; legacy/board fallbacks use .card-art.
    var artEl = rootEl.querySelector('.cf2-artbg, .card-art');
    if (!artEl || artEl.dataset.fullArt === '1') return;
    var img = new Image();
    img.onload = function() {
        if (artEl.dataset.fullArt === '1') return;
        artEl.style.backgroundImage = 'url(' + _cardArtUrl(cardId, true) + ')';
        artEl.dataset.fullArt = '1';
    };
    img.src = _cardArtUrl(cardId, true);
}

