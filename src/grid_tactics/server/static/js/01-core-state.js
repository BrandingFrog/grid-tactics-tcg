function showScreen(screenId) {
    document.querySelectorAll('.screen').forEach(function(el) {
        el.classList.remove('active');
    });
    var target = document.getElementById(screenId);
    if (target) {
        target.classList.add('active');
    }
    // Update nav button active state
    document.querySelectorAll('.nav-btn').forEach(function(btn) {
        if (btn.dataset.screen === screenId.replace('screen-', '')) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    // Render deck builder when switching to it
    if (screenId === 'screen-deck-builder' && allCardDefs) {
        renderDeckBuilder();
    }
    // Phase 14.6: Sandbox screen activation / deactivation
    if (screenId === 'screen-sandbox') {
        if (!sandboxMode) {
            sandboxActivate();
        }
        testsExit();
    } else if (screenId === 'screen-tests') {
        // Tests piggybacks on the sandbox screen DOM. We flip the sandbox
        // screen on (so the board renders + sandbox_state messages fire)
        // and then layer the tests overlay on top.
        var sbScreen = document.getElementById('screen-sandbox');
        if (sbScreen) sbScreen.classList.add('active');
        if (!sandboxMode) sandboxActivate();
        testsActivate();
    } else {
        if (sandboxMode) {
            sandboxDeactivate();
        }
        testsExit();
    }
}
window.showScreen = showScreen;

function setupNavHandlers() {
    document.querySelectorAll('.nav-btn').forEach(function(btn) {
        btn.addEventListener('click', function() {
            // Block navigation while in an active game
            if (isInActiveGame()) {
                return;
            }
            // Close the mobile hamburger menu (if open) on any tap.
            document.querySelector('.hud-header')?.classList.remove('hud-nav-open');
            var toggle = document.getElementById('hud-nav-toggle');
            if (toggle) toggle.setAttribute('aria-expanded', 'false');
            var screenName = btn.dataset.screen;
            if (screenName) {
                showScreen('screen-' + screenName);
            }
        });
    });

    // Hamburger toggle for the mobile-width nav drawer.
    var toggle = document.getElementById('hud-nav-toggle');
    var header = document.querySelector('.hud-header');
    if (toggle && header) {
        toggle.addEventListener('click', function(e) {
            e.stopPropagation();
            var opened = header.classList.toggle('hud-nav-open');
            toggle.setAttribute('aria-expanded', opened ? 'true' : 'false');
        });
        // Close the drawer on outside click so a second tap doesn't require
        // the hamburger specifically.
        document.addEventListener('click', function(e) {
            if (!header.classList.contains('hud-nav-open')) return;
            if (header.contains(e.target)) return;
            header.classList.remove('hud-nav-open');
            toggle.setAttribute('aria-expanded', 'false');
        });
    }
}

